     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = $request->validate([
            'user_id' => 'required',
            'city' => 'required',
            'tol' => 'required',
            'phone' => ['integer', 'digits:10'],
            'product_id' => 'required',
            'size' => 'required',
            'status' => 'nullable',
            'quantity' => 'required',
            'color' => 'required',
            'totalprice' => 'required',
        ]);
        

        Order::create($data);
    }

